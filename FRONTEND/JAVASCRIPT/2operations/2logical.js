// logical operations
// and -> &&
let isLoggedin = true;
let isPaid = true;
let isNotPaid = false;
console.log(isLoggedin && isPaid); // output -> true
console.log(isPaid && isNotPaid); // output -> false

// or -> ||
console.log(isLoggedin || isPaid); // output -> true

// not -> !
console.log(!isLoggedin); // output -> false